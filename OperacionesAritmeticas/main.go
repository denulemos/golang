package main

import "fmt"

func main() {
  x := 42
  y := 8
      
  // Addition
  res := x + y
  fmt.Println(res)
      
  // Subtraction
  res = x - y
  fmt.Println(res)
      
  // Multiplication
  res = x * y
  fmt.Println(res)
      
  // Division
  res = x / y
  fmt.Println(res)
      
  // Modulus, results in the remainder of the division
  res = x % y
  fmt.Println(res) 
}
